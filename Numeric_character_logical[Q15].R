# Create three vectors
numeric_vector <- c(1.5, 2.7, 3.0, 4.2)
character_vector <- c("apple", "banana", "orange", "grape")
logical_vector <- c(TRUE, FALSE, TRUE, FALSE)

# Display the content of each vector
cat("Numeric Vector:\n")
print(numeric_vector)

cat("\nCharacter Vector:\n")
print(character_vector)

cat("\nLogical Vector:\n")
print(logical_vector)

# Display the data type of each vector
cat("\nData Type of Numeric Vector: ", typeof(numeric_vector), "\n")
cat("Data Type of Character Vector: ", typeof(character_vector), "\n")
cat("Data Type of Logical Vector: ", typeof(logical_vector), "\n")
